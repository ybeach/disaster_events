        <h2>はじめに（藤木久志 編『日本中世気象災害史年表稿』（高志書院）より）</h2>
        <p>
            私が還暦を迎えた1993年は，この列島社会が厳しい冷害と凶作に見舞われ，ことに北国では，稲作の収量が，平年作の3分の1ほどにまで落ち込むという，悲惨な状況が日々に報じられていた。この深
            刻な冷害・凶作情報は，私に大きな衝撃を与えた。<br />
            これまで私は，日本中世史を学ぶなかで，大きな旱魃や風水害による凶作や飢饉や疫病が，中世社会にも，いくども起きていたことを知っていながら，それらを，偶発的で個別的な小事件と見なすだけで，それらを日本中世全体のおかれた，厳しい気象状況の中に位置付けることには，まったく無関心であっ
            たからである。<br />
            この深刻な反省をもとに，私は還暦を機に，晩年の小さな宿題として，中世社会の風水害や旱魃，虫損， それらを原因とするとみられる，凶作や飢饉や疫病の情報を，中世の記録や古文書の中から収集する作
            業にとりかかった。<br />
            なお，災害といえば，地震や噴火を無視することはできないが，これらについては，すでに先学によっ
            て貴重な集大成が行われているので，私は災害史料の収集を，上記のような気象関連の災害に限ることとした。ただ，直接に気象災害に注目するだけでなく，それを示唆する，百姓の損免要求など，間接的
            な情報にも目を向けることにした。<br />
            史料の検索は限りがなく，私の狭い知見の範囲を超える，大きな広がりがある。従って本書は，中世 気象災害の総括というには，ほど遠い。本書を「日本中世気象災害史年表稿」と限定したのは，そのためである。作業を始めてから 15
            年にわたるので，ここで一区切りしようと，ふと思い立ったに過ぎない。<br />
            日本中世をどの範囲とするかは，むづかしい問題であるが，ここでは，かりに10世紀初めから，17 世紀を少し越えるあたりまでを対象とした。<br />
